class field:

    def __init__(self, startpos):
        self.__h = len(startpos)
        self.__w = len(startpos[0])
        self.__startpos = startpos
        self.__currIter = 0
        self.__cache = {0: startpos}
        self.__minIter = 0

    def __checkCell(self, list, i, j):
        h = len(list)
        w = len(list[0])
        get = lambda i, j: list[i % h][j % w]
        s = get(i-1, j-1) + get(i-1, j) + get(i-1, j+1) +\
            get(i  , j-1) +               get(i  , j+1) +\
            get(i+1, j-1) + get(i+1, j) + get(i+1, j+1)
        if s == 3:
            return 1
        elif s == 2:
            return get(i, j)
        else:
            return 0

    def __findPrev(self, target):
        def checkLine(lineNum):
            fits = True
            for l in xrange(self.__w):
                if self.__checkCell(cand, lineNum, l) != target[lineNum][l]:
                    fits = False
                    break
            return fits

        def generateLists(len):
            for num in xrange(2 ** len):
                line = []
                temp = num
                for i in xrange(len):
                    line.append(temp & 1)
                    temp >>= 1
                yield line

        newCands = []

        for i in generateLists(self.__w):
            for j in generateLists(self.__w):
                for k in generateLists(self.__w):
                    cand = [list(i), list(j), list(k)]
                    if checkLine(1):
                        newCands.append(cand)

        for i in xrange(3, self.__h):
            oldCands = newCands
            newCands = []

            for j in oldCands:
                for k in generateLists(self.__w):
                    cand = list(j)
                    cand.append(list(k))
                    if checkLine(i - 1):
                        newCands.append(cand)

        for cand in newCands:
            if checkLine(self.__h - 1) and checkLine(0):
                return cand

        return None

    def generate(self, num, findPrev = False):
        if num < self.__minIter - 1:
            return "Overflow"

        elif num == self.__minIter - 1:
            if findPrev:
                prev = self.__findPrev(self.__cache[self.__minIter])
                if prev is None:
                    return "No prev"
                else:
                    self.__cache[num] = prev
                    self.__minIter = num
            else:
                return "Prev warning"
        else:
            bestKey = num
            keys = self.__cache.keys()
            while not (bestKey in keys):
                bestKey -= 1

            curr = self.__cache[bestKey]
            for step in xrange(bestKey, num):
                dest = [[]] * self.__h
                for i in xrange(0, self.__h):
                    dest [i] = [0] * self.__w

                for i in xrange(0, self.__h):
                    for j in xrange(0, self.__w):
                        dest[i][j] = self.__checkCell(curr, i, j)

                curr = dest
                self.__cache[step] = curr
            self.__cache[num] = curr

        self.__currIter = num
        return "OK"

    def getField(self):
        return self.__cache[self.__currIter]

    def getCurrIter(self):
        return self.__currIter



